#include "3-calc.h"

/**
 * op_add - returns the sum of two integers
 * @a: take first integer
 * @b: take second integer
 *
 * Return: sum of a and b
 */
int op_add(int a, int b);

int op_add(int a, int b)
{
	return a + b;
}
/**
 * op_sub - returns the difference of two integers
 * @a: takes first integer
 * @b: takes second integer
 *
 * Return: difference of a and b
 */
int op_sub(int a, int b);

int op_sub(int a, int b);
{
	return a - b;
}

/**
 * op_mul - returns the product of two integers
 * @a: takes first integer
 * @b: takes second integer
 *
 * Return: product of a and b
 */
int op_mul(int a, int b);

int op_mul(int a, int b)
{
	return a * b;
}


/**
 * op_div - returns the quotient of two integers
 * @a: takes first integer value
 * @b: takes second integer value
 *
 * Return: quotient of a and b
 */

int op_div(int a, int b);

int op_div(int a, int b)
{
	return a / b;
}

/**
 * op_mod - returns the remainder of two integers
 *
 * @a: takes first integer value
 * @b: takes second integer value
 *
 * Return: reaminder of a and b
 */

int op_mod(int a, int b);

int op_mod(int a, int b)
{
	return a % b;
}
